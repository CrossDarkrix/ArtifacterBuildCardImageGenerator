def get_player_info(uid):
    api_url = 'https://enka.network/api/uid/{}?info'.format(uid)
    response = requests.get(api_url)
    json_response = json.loads(response.text)
    store_character = json.loads(Store_character())
    store_locale = json.loads(Store_locale_Ja())
    playerInfo = json_response['playerInfo']
    if 'showAvatarInfoList' in playerInfo:
        for avatar in playerInfo['showAvatarInfoList']:
            avatar['name'] = store_locale[str(store_character[str(avatar['avatarId'])]['NameTextMapHash'])]

    return playerInfo

def get_avatar_info(uid, avatar_id):
    api_url = 'https://enka.network/api/uid/{}'.format(uid)

    response = requests.get(api_url)
    json_response = json.loads(response.text)
    avatar_data = []
    if 'avatarInfoList' in json_response:
        avatar_data = list(filter(lambda item : item['avatarId'] == avatar_id, json_response['avatarInfoList']))[0]

    return avatar_data

def decimal_to_float(obj):
    if isinstance(obj, Decimal):
        return float(obj)

def create_json(uid, avatarInfo, score_state):
    model = json.loads(FormatJson())
    store_character = json.loads(Store_character())
    store_locale = json.loads(Store_locale_Ja())
    model['uid'] = uid
    model['input'] = ''
    elementLocale = {'Fire':'炎', 'Water':'水', 'Electric':'雷', 'Ice':'氷', 'Wind':'風', 'Rock':'岩', 'Grass':'草'}
    model['元素'] = elementLocale[store_character[str(avatarInfo['avatarId'])]['Element']]
    character = model['Character']
    character['Name'] = store_locale[str(store_character[str(avatarInfo['avatarId'])]['NameTextMapHash'])]
    character['Const'] = int(avatarInfo['propMap']['1002']['val'])
    character['Level'] = int(avatarInfo['propMap']['4001']['val'])
    character['Love'] = int(avatarInfo['fetterInfo']['expLevel'])
    character['Status']['HP'] = int(Decimal(avatarInfo['fightPropMap']['2000']).quantize(Decimal('0'), rounding=ROUND_HALF_UP))
    character['Status']['攻撃力'] = int(Decimal(avatarInfo['fightPropMap']['2001']).quantize(Decimal('0'), rounding=ROUND_HALF_UP))
    character['Status']['防御力'] = int(Decimal(avatarInfo['fightPropMap']['2002']).quantize(Decimal('0'), rounding=ROUND_HALF_UP))
    character['Status']['元素熟知'] = int(Decimal(avatarInfo['fightPropMap']['28']).quantize(Decimal('0'), rounding=ROUND_HALF_UP))
    character['Status']['会心率'] = Decimal(avatarInfo['fightPropMap']['20'] * 100).quantize(Decimal('0.1'), rounding=ROUND_HALF_UP)
    character['Status']['会心ダメージ'] = Decimal(avatarInfo['fightPropMap']['22'] * 100).quantize(Decimal('0.1'), rounding=ROUND_HALF_UP)
    character['Status']['元素チャージ効率'] = Decimal(avatarInfo['fightPropMap']['23'] * 100).quantize(Decimal('0.1'), rounding=ROUND_HALF_UP)
    buffLists = {'30':'物理ダメージ', '40':'炎元素ダメージ', '41':'雷元素ダメージ', '42':'水元素ダメージ', '43':'草元素ダメージ', '44':'風元素ダメージ', '45':'岩元素ダメージ', '46':'氷元素ダメージ'}
    buffKeys = list(buffLists.keys())
    maxKey = '30'
    maxValue = 0
    for key in buffKeys:
        if avatarInfo['fightPropMap'][key] > maxValue:
            maxValue = avatarInfo['fightPropMap'][key]
            maxKey = key
    character['Status'][buffLists[maxKey]] = Decimal(avatarInfo['fightPropMap'][maxKey] * 100).quantize(Decimal('0.1'), rounding=ROUND_HALF_UP)
    if 'proudSkillExtraLevelMap' in avatarInfo:
        skillKeys = list(map(int, avatarInfo['skillLevelMap'].keys()))
        proudKeys = list(map(int, avatarInfo['proudSkillExtraLevelMap'].keys()))
        proudMap = store_character[str(avatarInfo['avatarId'])]['ProudMap']
        for key in skillKeys:
            if proudMap[str(key)] in proudKeys:
                avatarInfo['skillLevelMap'][str(key)] += avatarInfo['proudSkillExtraLevelMap'][str(proudMap[str(key)])]
    character['Talent']['通常'] = avatarInfo['skillLevelMap'][str(store_character[str(avatarInfo['avatarId'])]['SkillOrder'][0])]
    character['Talent']['スキル'] = avatarInfo['skillLevelMap'][str(store_character[str(avatarInfo['avatarId'])]['SkillOrder'][1])]
    character['Talent']['爆発'] = avatarInfo['skillLevelMap'][str(store_character[str(avatarInfo['avatarId'])]['SkillOrder'][2])]
    character['Base']['HP'] = Decimal(avatarInfo['fightPropMap']['1']).quantize(Decimal('0'), rounding=ROUND_HALF_UP)
    character['Base']['攻撃力'] = Decimal(avatarInfo['fightPropMap']['4']).quantize(Decimal('0'), rounding=ROUND_HALF_UP)
    character['Base']['防御力'] = Decimal(avatarInfo['fightPropMap']['7']).quantize(Decimal('0'), rounding=ROUND_HALF_UP)
    weapon = model['Weapon']
    weaponInfo = list(filter(lambda item : item.get('weapon'), avatarInfo['equipList']))[0]
    weapon['name'] = store_locale[weaponInfo['flat']['nameTextMapHash']]
    weapon['Level'] = weaponInfo['weapon']['level']
    weapon['totu'] =list(weaponInfo['weapon']['affixMap'].values())[0] + 1
    weapon['rarelity'] = weaponInfo['flat']['rankLevel']
    weapon['BaseATK'] = list(filter(lambda item : item['appendPropId'] == 'FIGHT_PROP_BASE_ATTACK', weaponInfo['flat']['weaponStats']))[0]['statValue']
    weapon['Sub']['name'] = store_locale[list(filter(lambda item : item['appendPropId'] != 'FIGHT_PROP_BASE_ATTACK', weaponInfo['flat']['weaponStats']))[0]['appendPropId']]
    weapon['Sub']['value'] = list(filter(lambda item : item['appendPropId'] != 'FIGHT_PROP_BASE_ATTACK', weaponInfo['flat']['weaponStats']))[0]['statValue']
    score = model['Score']
    score['State'] = score_state
    state_list = {'HP':'HPパーセンテージ', '攻撃':'攻撃パーセンテージ', '防御':'防御パーセンテージ', 'チャージ':'元素チャージ効率', '元素熟知':'元素熟知'}
    relicTotalScore = 0
    relicList = list(filter(lambda item : item.get('reliquary'), avatarInfo['equipList']))
    relicType = {'EQUIP_BRACER':'flower', 'EQUIP_NECKLACE':'wing', 'EQUIP_SHOES':'clock', 'EQUIP_RING':'cup', 'EQUIP_DRESS':'crown'}
    for relicInfo in relicList:
        relic = model['Artifacts'][relicType[relicInfo['flat']['equipType']]]
        relic['type'] = store_locale[relicInfo['flat']['setNameTextMapHash']]
        relic['Level'] = relicInfo['reliquary']['level'] - 1
        relic['rarelity'] = relicInfo['flat']['rankLevel']
        relic['main']['option'] = store_locale[relicInfo['flat']['reliquaryMainstat']['mainPropId']]
        relic['main']['value'] = relicInfo['flat']['reliquaryMainstat']['statValue']
        relicScore = 0
        for index, substats in enumerate(relicInfo['flat']['reliquarySubstats']):
            relic['sub'].append({})
            relic['sub'][index]['option'] = store_locale[substats['appendPropId']]
            relic['sub'][index]['value'] = substats['statValue']
            if relic['sub'][index]['option'] in [state_list[score_state], '会心ダメージ']:
                if relic['sub'][index]['option'] == '元素熟知':
                    relicScore += Decimal(relic['sub'][index]['value'] * 0.25)
                else:
                    relicScore += Decimal(relic['sub'][index]['value'])
            elif relic['sub'][index]['option'] in ['会心率']:
                relicScore += Decimal(relic['sub'][index]['value'] * 2)
            score[relicType[relicInfo['flat']['equipType']]] = Decimal(relicScore).quantize(Decimal('0.1'), rounding=ROUND_HALF_UP)
        relicTotalScore += Decimal(relicScore)
    score['total'] = relicTotalScore
    os.makedirs('json', exist_ok=True)
    with open(os.path.join(os.getcwd(), 'json', 'data.json'), 'w', encoding='utf-8') as f:
        json.dump(model, f, indent=2, ensure_ascii=False, default=decimal_to_float)
    return model
