def LocaleJa():
    LJsn = json.loads(concurrent.futures.ThreadPoolExecutor().submit(urllib.request.urlopen, urllib.request.Request('https://raw.githubusercontent.com/EnkaNetwork/API-docs/master/store/loc.json', headers={'User-Agent': UsrAgn})).result().read().decode(errors='ignore'))
    for percent in [ja for ja in LJsn['ja'] if 'PERCENT' in ja]:
        if '力' in LJsn['ja'][percent]:
            LJsn['ja'][percent] = LJsn['ja'][percent].replace('力', 'パーセンテージ')
        else:
            LJsn['ja'][percent] = LJsn['ja'][percent].replace(LJsn['ja'][percent][-1], '{}パーセンテージ'.format(LJsn['ja'][percent][-1]))
    return json.dumps(LJsn, ensure_ascii=False)

